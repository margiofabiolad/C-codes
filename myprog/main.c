#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("Hello world!\n");
    printf("Hello world!\n");
    printf("%d \n", 32); /// INTEGER
    printf("%ld \n", 3298753265); /// LARGE INTEGER
    printf("%f \n", 32.2); /// float
    printf("%lf \n", 32889855655.4); /// large float
    printf("%c \n", 'a'); /// char
    printf("%s \n", "hola"); /// string
    printf("%x \n", 16); /// hex


    return 0;
}
